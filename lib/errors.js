var util = require('util');

//----------------------------------------------------------------------------------------------------------------------

function RequestDenied(message)
{
    Error.call(this);

    this.message = message;
    this.reason = message.reason;
} // end ListenFailure

util.inherits(RequestDenied, Error);

//----------------------------------------------------------------------------------------------------------------------

module.exports = {
    RequestDenied: RequestDenied
}; // end exports

